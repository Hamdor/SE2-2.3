#ifndef SE2_TOKEN_HPP
#define SE2_TOKEN_HPP

namespace se2 {

struct token {
  int m_id;
  int m_wert1;
  int m_wert2;
  bool is_valid() const {
    return !(m_id == 0 && m_wert1 == 0 && m_wert2 == 0);
  }
};

} // namespace se2

#endif // SE2_TOKEN_HPP
